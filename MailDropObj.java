public class MailDropObj {
    int mailDropID;
    String mailDropAddress;
    String mailDropBusinessName;
    String notes;

    public int getMailDropID() {
        return mailDropID;
    }

    public void setMailDropID(int mailDropID) {
        this.mailDropID = mailDropID;
    }

    public String getMailDropAddress() {
        return mailDropAddress;
    }

    public void setMailDropAddress(String mailDropAddress) {
        this.mailDropAddress = mailDropAddress;
    }

    public String getMailDropBusinessName() {
        return mailDropBusinessName;
    }

    public void setMailDropBusinessName(String mailDropBusinessName) {
        this.mailDropBusinessName = mailDropBusinessName;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }


}
